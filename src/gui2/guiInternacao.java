    private void cboEspecialidadeItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cboEspecialidadeItemStateChanged
        // TODO add your handling code here:
        Especialidade e = (Especialidade) cboEspecialidade.getSelectedItem();

        if(e==null) return;

        List list = new ProfissionalJpaController().getListProfissional(e);
        cboMedico.removeAllItems();
        for (int i = 0; i < list.size(); i++) {
            Profissional p = (Profissional)list.get(i);
            cboMedico.addItem(p);
        }
        
    }//GEN-LAST:event_cboEspecialidadeItemStateChanged

