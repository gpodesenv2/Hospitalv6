    private void bntGravar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bntGravar1ActionPerformed
        // TODO add your handling code here:
        Internacao a = new Internacao();

        a.setEspecialidade((Especialidade) cboEspecialidade.getSelectedItem());
        a.setMedico((Profissional)cboMedico.getSelectedItem());
        a.setHora(txtHora.getText());        
        a.setProced((Procedimento)cboProced.getSelectedItem());
        a.setCid((CID)cboCID.getSelectedItem());
        a.setLeito((Leito)cboLeito.getSelectedItem());
        a.setDataEntrada(dataInicio1.getCalendar());
        a.setProntuario(txtPront.getText());
        a.setMae(txtMae.getText());
        a.setNome(txtNome.getText());
        a.setTelefone(txtTelefone.getText());
        a.setCpf(txtCPF.getText());
        a.setDataNasc(txtDataNasc.getText());
        a.setTipoInter((String)cboTipo.getSelectedItem());
        a.setLocal((Localizacao)cboLocal.getSelectedItem());
        
        List in = new InternacaoJpaController().getPaciente(txtPront.toString());
        if (!(in.isEmpty())) {
            JOptionPane.showMessageDialog(null, "Já foi efetuada internação para este nº de prontuário");            
            return;
        }
        
        List ags = new InternacaoJpaController().getListInternacao((Leito)cboLeito.getSelectedItem());

        if (!(ags.isEmpty())) {
            JOptionPane.showMessageDialog(null, "Leito ocupado");            
            return;
        }

        new InternacaoJpaController().create(a);
        JOptionPane.showMessageDialog(null,"Internação efetuada\n \tCod: "+ a.getId());
            txtInt.setText("");
            txtNome.setText("");
            txtCPF.setText("");
            txtDataNasc.setText("");
            txtMae.setText("");
            txtTelefone.setText("");
            txtHora.setText("");
            txtPront.setText("");
            dataInicio1.setDate(null);
            cboEspecialidade.setSelectedIndex(0);
            cboProced.setSelectedIndex(0);
            cboMedico.setSelectedIndex(0);
            cboLeito.setSelectedIndex(0);
            cboCID.setSelectedIndex(0);
            cboLocal.setSelectedIndex(0);
            cboTipo.setSelectedIndex(0);
    }//GEN-LAST:event_bntGravar1ActionPerformed

