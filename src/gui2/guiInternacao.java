    private void formInternalFrameActivated(javax.swing.event.InternalFrameEvent evt) {//GEN-FIRST:event_formInternalFrameActivated
        // TODO add your handling code here:
        List l = new EspecialidadeJpaController().findEspecialidadeEntities();
        cboEspecialidade.removeAllItems();
        for (int i=0;i<l.size();i++) {
        cboEspecialidade.addItem(l.get(i));
        }
        
        
        
        List lCid = new CIDJpaController().findCIDEntities();
        cboCID.removeAllItems();
        for (int i = 0; i < lCid.size(); i++) {
         cboCID.addItem(lCid.get(i));
        }

        List lLocal = new LocalizacaoJpaController().findLocalizacaoEntities();
        cboLocal.removeAllItems();
        for (int i = 0; i < lLocal.size(); i++) {
         cboLocal.addItem(lLocal.get(i));
        }
          
        
        txtPront.grabFocus();
    }//GEN-LAST:event_formInternalFrameActivated

