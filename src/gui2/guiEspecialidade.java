    private void bntGravarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bntGravarActionPerformed

        Especialidade e = new Especialidade();
        e.setNome(txtNome.getText());

        new EspecialidadeJpaController().create(e);

        JOptionPane.showMessageDialog(null,"Especialidade cadastrada\n \tID: " + e.getId());
        // txtCodigo.setText(e.getId().toString());
        
        txtNome.setText("");
        lstEspecialidade.removeAll();
        List l = new EspecialidadeJpaController().getEspecialidade();
        lstEspecialidade.setListData(l.toArray());
        
        txtNome.grabFocus(); // #Retorna o foco#     
        
                
    }//GEN-LAST:event_bntGravarActionPerformed

