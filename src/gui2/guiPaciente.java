    private void bntEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bntEditarActionPerformed
        // TODO add your handling code here:
        if (paciente==null) return;

        paciente.setBairro((Bairro) cboBairro.getSelectedItem());
        paciente.setRua(txtRua.getText());
        paciente.setCpf(txtCpf.getText());
        paciente.setNome(txtNome.getText());
        paciente.setNumero(txtNumero.getText());
        paciente.setTelefone(txtTelefone.getText());
        paciente.setDataNasc(txtDataNasc.getText());
        paciente.setIdentidade(txtRg.getText());
        paciente.setCertidao(txtCertidao.getText());
        paciente.setPai(txtPai.getText());
        paciente.setMae(txtMae.getText());
        paciente.setCep(txtCep.getText());
        paciente.setComplemento(txtComp.getText());
        paciente.setSexo(escolheSexo());

        try {
            new PacienteJpaController().edit(paciente);

        } catch (NonexistentEntityException ex) {
            JOptionPane.showMessageDialog(null, "Paciente inexistente");
            codProntuario.setText("");

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage() + " Procure o Administrador do Sistema");
        }

        JOptionPane.showMessageDialog(null,"Dados de Paciente Alterados");

        codProntuario.setText("");
        txtCpf.setText("");
        txtNumero.setText("");
        txtRua.setText("");
        txtNome.setText("");
        txtTelefone.setText("");
        txtDataNasc.setText("");
        txtRg.setText("");
        txtCertidao.setText("");
        txtPai.setText("");
        txtMae.setText("");
        txtCep.setText("");
        txtComp.setText("");
        cboBairro.setSelectedIndex(0);
        cboCidade.setSelectedIndex(0);
        cboEstado.setSelectedIndex(0);
    }//GEN-LAST:event_bntEditarActionPerformed

