        txtCep.setText(paciente.getCep().toString());
        txtPai.setText(paciente.getPai().toString());
        txtMae.setText(paciente.getMae().toString());
        txtCertidao.setText(paciente.getCertidao().toString());
        txtComp.setText(paciente.getComplemento().toString());
        setaSexo(paciente.getSexo());
        
        cboEstado.setSelectedItem(paciente.getBairro().getCidade().getEstado());
        cboCidade.setSelectedItem(paciente.getBairro().getCidade());
        cboBairro.setSelectedItem(paciente.getBairro());

