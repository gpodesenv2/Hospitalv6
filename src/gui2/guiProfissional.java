    private void cboEstadoItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cboEstadoItemStateChanged
        Estado e = (Estado) cboEstado.getSelectedItem();

        if(e==null) return;
        List l = new CidadeJpaController().getListCidadeEstado(e);
        cboCidade.removeAllItems();
        for (int i = 0; i < l.size(); i++) {
            Cidade c = (Cidade)l.get(i);
            cboCidade.addItem(c);
        }

    }//GEN-LAST:event_cboEstadoItemStateChanged

    private void cboCidadeItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cboCidadeItemStateChanged
        Cidade c = (Cidade) cboCidade.getSelectedItem();

        if(c==null) return;
        List l = new BairroJpaController().getListCidadeBairro(c);
        cboBairro.removeAllItems();
        for (int i = 0; i < l.size(); i++) {
            Bairro b = (Bairro)l.get(i);
            cboBairro.addItem(b);
        }
    }//GEN-LAST:event_cboCidadeItemStateChanged

