    private void burcarAgendActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_burcarAgendActionPerformed
        // TODO add your handling code here:
        paciente = new PacienteJpaController().getPaciente(txtPront.getText());

        txtNome.setText(paciente.getNome().toString());

        try {
            List l = new AgendamentoJpaController().getAgendamentoPac(txtPront.getText());
            lstPaciente.setListData(l.toArray());

            if(l.isEmpty()){
                JOptionPane.showMessageDialog(null, "N√£o foi agendada Cirurgia com este n√∫mero de prontu√°rio");
                txtNome.setText("");
                txtPront.setText("");    
                txtDataCirurgia.setDate(null);
                txtCirurgia.setText("");
            lstPaciente.setModel(new DefaultListModel());
            lstMedicamento.setModel(new DefaultListModel());
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "N√£o foi agendada Cirurgia com este n√∫mero de prontu√°rio");
            txtNome.setText("");
            txtPront.setText("");
            txtDataCirurgia.setDate(null);
            txtCirurgia.setText("");
            lstPaciente.setModel(new DefaultListModel());
            lstMedicamento.setModel(new DefaultListModel());
        }

    }//GEN-LAST:event_burcarAgendActionPerformed

    private void lstPacienteValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_lstPacienteValueChanged
        // TODO add your handling code here:

        Agendamento a  = (Agendamento)lstPaciente.getSelectedValue();

        txtPront.setText(a.getPaciente().getId().toString());
        txtNome.setText(a.getPaciente().getNome().toString());
        txtDataCirurgia.setDate(a.getDataInicio().getTime());
        txtCirurgia.setText(a.getProcedimento().toString());

        lstMedicamento.removeAll();
        List l = new CirurgiaMedicamentoJpaController().getMedicamentos(a.getId().toString());
        lstMedicamento.setListData(l.toArray());

    }//GEN-LAST:event_lstPacienteValueChanged

    private void bntAdicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bntAdicionarActionPerformed

        CirurgiaMedicamento cm = new CirurgiaMedicamento();
        ag =(Agendamento)lstPaciente.getSelectedValue();

        cm.setAgendamento(ag);
        cm.setDataCirurgia(txtDataCirurgia.getCalendar());
        cm.setMedicamento((Medicamento)cboMedicamentos.getSelectedItem());
        cm.setPeriodo(txtPeriodo.getText());

        new CirurgiaMedicamentoJpaController().create(cm);
        JOptionPane.showMessageDialog(null,"Novo Medicamento adicionado a Cirurgia '"+ ag.getProcedimento()+"'\nPaciente: " +ag.getPaciente().getNome().toString());
        lstMedicamento.removeAll();
        List l = new CirurgiaMedicamentoJpaController().getMedicamentos(ag.getId().toString());
        lstMedicamento.setListData(l.toArray());
        //lstMedicamento.setModel(new DefaultListModel());

    }//GEN-LAST:event_bntAdicionarActionPerformed

